let week = Number(prompt('enter week day: '))
if(week == 1) {
    console.log('Monday')
}
else if(week == 2) {
    console.log('Tuesday')
}
else if(week == 3) {
    console.log('Wednesday')
}
else if(week == 4) {
    console.log('Thursday')
}
else if(week == 5) {
    console.log('Friday')
}
else if(week == 6) {
    console.log('Saturday')
}
else if(week == 7) {
    console.log('Sunday')
}
else{
    console.log('enter valid week day')
}