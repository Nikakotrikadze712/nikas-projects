const num1 = Number(prompt("შეიყვანეთ პირველი რიცხვი:"));
const num2 = Number(prompt("შეიყვანეთ მეორე რიცხვი:"));

if (num1 > 0 && num2 > 0) {
    if (num1 + num2 > 100) {
        alert("ორივე რიცხვი დადებითია და მათი ჯამი მეტია 100-ზე.");
    } else {
        alert("ორივე რიცხვი დადებითია, მაგრამ მათი ჯამი 100-ზე ნაკლებია ან ტოლია.");
    }
} else {
    alert("ორივე რიცხვი დადებითი არ არის.");
}